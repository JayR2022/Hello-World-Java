package com.jr.datatypes;

public class DataTypes {
	
	public static void printMessage(String message) {
		System.out.println(message);
	}
	
	public static String returnString(String message) {
		return message;
	}
	
	public static boolean returnBoolean() {
		boolean myStatus = true;
		
		return myStatus;
		
	}
	
	public static float returnMyFloat(int myIntNumber) {
		float myFloatNumber = myIntNumber;
		return myFloatNumber;
	}
	
	public static int returnMyChar2Int(char myChar) {
		 int myInt = myChar;
		return myInt;
	}

}
